PI = 3.14 #pi value

#user-input
r = float(input('Enter the radius of a circle: '))

#Area formula
area = PI * r * r
#Circumference formula
circumference = 2 * PI * r

#printing the outputs
print("\n Area Of a Circle = %.2f" %area)
print(" Circumference Of a Circle = %.2f" %circumference)